//change size of image
function imgSize(x)
{
  if (x.style.width == "300px" && x.style.height == "200px")
  {
    x.style.width = "1000px";
    x.style.height = "666px";
  }
  else if (x.style.width == "1000px" && x.style.height == "666px")
  {
    x.style.width = "300px";
    x.style.height = "200px";
  } //end if
} //end of imgSize function
